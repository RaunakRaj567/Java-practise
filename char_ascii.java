public class char_ascii {
    public static void main(String[] args) {
        char ch1='A';
        char ch2='Z';

        char ch3= 'a';
        char ch4= 'z';

        int a=ch1;
        int b=ch2;
        int c=ch3;
        int d=ch4;

        System.out.println(ch1);
        System.out.println(ch2);
        System.out.println(ch3);
        System.out.println(ch4);

        System.out.println(a); //ASCII values
        System.out.println(b); //ASCII value
        System.out.println(c); //ASCII value
        System.out.println(d); //ASCII value
    }
    
}
