public class division_in_different_data_types {
    public static void main(String[] args) {
        int x=56;
        int y =3;
        double a =34.32;
        double b =12.51;

        System.out.println(x/y); // int/int=int
         System.out.println(x/a); // int/double=double  or vice versa
          System.out.println(a/b);//double/double=double
           
    }
    
}
